//!
//! Tier 1 (this slice): images only — a provider trait
//! ([`provider::ImageProvider`]) with Gemini + OpenAI backends, a
//! [`registry`] that maps a model string to its provider, and shared
//! output helpers. The `TextToImage` / `ImageToImage` tools in
//! `tools/image_gen.rs` and (Tier 3) the Media Studio shell both drive
//! generation through this module, so a generation triggered by the
//! model and one triggered from the GUI are byte-identical and bill
