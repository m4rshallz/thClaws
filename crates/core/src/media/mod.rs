/// Detect image format from magic bytes. Generated bytes aren't always
/// PNG (Gemini flash often returns JPEG); mislabelling JPEG as PNG makes
/// Anthropic reject the inline image block.
pub fn sniff_ext(bytes: &[u8]) -> &'static str {
    if bytes.starts_with(&[0x89, b'P', b'N', b'G']) {
        "png"
    } else if bytes.starts_with(&[0xFF, 0xD8, 0xFF]) {
        "jpg"
    } else if bytes.len() > 11 && &bytes[8..12] == b"WEBP" {
        "webp"
    } else {
        "png"
    }
}

/// Save image bytes under `output/img-<ts>-<sha8>.<ext>` and return the
/// relative path. Creates `output/` if missing; the sha prefix makes
/// collisions vanishingly unlikely so we never overwrite.
pub fn save_image(bytes: &[u8], ext: &str) -> Result<PathBuf> {
    let sha = Sha256::digest(bytes);
    let sha_hex = format!("{:02x}{:02x}{:02x}{:02x}", sha[0], sha[1], sha[2], sha[3]);
    let ts = chrono::Utc::now().format("%Y%m%d-%H%M%S");
    let name = format!("img-{ts}-{sha_hex}.{ext}");
    let dir = std::path::Path::new("output");
    std::fs::create_dir_all(dir).map_err(|e| Error::Tool(format!("mkdir output: {e}")))?;
    let path = dir.join(&name);
    std::fs::write(&path, bytes)
        .map_err(|e| Error::Tool(format!("write {}: {e}", path.display())))?;
    Ok(path)
}

/// Detect video container from magic bytes. Veo returns MP4 (ISO-BMFF:
/// `....ftyp` at offset 4); fall back to mp4.
pub fn sniff_video_ext(bytes: &[u8]) -> &'static str {
    if bytes.len() > 11 && &bytes[4..8] == b"ftyp" {
        "mp4"
    } else if bytes.starts_with(&[0x1A, 0x45, 0xDF, 0xA3]) {
        "webm"
    } else {
        "mp4"
    }
}

